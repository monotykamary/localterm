import fs from "node:fs";
import path from "node:path";
import type { Process } from "./types.js";
import type { SecretBackend } from "./secret-backend.js";
import { ACTIVITY_WATCHED_PROGRAMS } from "./constants.js";

interface ProgramSecret {
  envVar: string;
  name: string;
}

// Build the index the shim generator consumes: process name -> the secrets that
// process should receive (in policy order, so the shim's env assignments are
// stable across regenerations). A process's requestedSecrets are resolved to
// {envVar, name} via `envVarByName`; a name that no longer exists in the store
// (a deleted secret the cascading delete missed, or a stale process file) is
// skipped so a shim never references a missing secret. A process whose
// requestedSecrets resolve to nothing (empty selection, or only deleted
// secrets) is omitted entirely so it gets no shim — matching the pre-flip
// behavior where a program no secret referenced had no shim, and the
// stale-shim sweep then removes any shim it previously had.
const buildProcessSecretIndex = (
  processes: readonly Process[],
  envVarByName: Map<string, string>,
): Map<string, ProgramSecret[]> => {
  const index = new Map<string, ProgramSecret[]>();
  for (const process of processes) {
    const resolved: ProgramSecret[] = [];
    for (const secretName of process.requestedSecrets) {
      const envVar = envVarByName.get(secretName);
      if (envVar) resolved.push({ envVar, name: secretName });
    }
    if (resolved.length > 0) index.set(process.name, resolved);
  }
  return index;
};

// The self-contained POSIX shim for one program. Strips its own directory from
// PATH so `command -v` resolves the REAL binary (not this shim — recursion),
// then resolves each secret via the backend's snippet and runs the real binary
// with the non-empty ones exported. Activity-watched programs (e.g. `gh`) pass
// `activityFile`: the shim runs the real binary as a child, then overwrites
// that file with the shell's $PWD so the daemon can react to the command having
// run (the signal fires after the binary exits, and never affects its exit
// code). All other programs `exec` the real binary directly. The shim runs in
// the PTY where the user's rc files have already built the full PATH (homebrew,
// mise, …), so `command -v <program>` finds the real binary the user installed;
// only the shims dir (prepended by localterm's shell hook AFTER the rc files
// run) is excluded. `_shim_dir` is baked as a literal so the strip-by-equality
// match is exact even if $HOME differs from the daemon's view.
export const buildShimContent = (
  program: string,
  shimsDir: string,
  resolveSnippet: string,
  activityFile?: string,
): string => {
  const escapedProgram = program.replace(/'/g, "'\\''");
  const escapedShimsDir = shimsDir.replace(/'/g, "'\\''");
  const lines: string[] = [
    "#!/bin/sh",
    "# localterm shim for " + program + ". Generated by localterm-server; do not edit.",
    "# Strips this shim's own directory from PATH so `command -v` resolves the",
    "# real binary instead of recursing into this shim. localterm's shell hook",
    "# prepends the shims dir after the user's rc files run, so it is first on PATH.",
  ];
  if (activityFile) {
    lines.push(
      "# Activity-watched: runs the real binary as a child, then overwrites the",
      "# daemon's activity file with the shell's $PWD so consumers react to the",
      "# command having run (event-driven, no polling). The signal is best-effort",
      "# and never affects the program's exit code.",
    );
  }
  lines.push("_shim_dir='" + escapedShimsDir + "'");
  if (activityFile) {
    lines.push("_activity_file='" + activityFile.replace(/'/g, "'\\''") + "'");
  }
  lines.push(
    "_rest=",
    "_oldifs=$IFS",
    "IFS=:",
    "for _d in $PATH; do",
    '  [ "$_d" = "$_shim_dir" ] && continue',
    "  _rest=${_rest:+$_rest:}$_d",
    "done",
    "IFS=$_oldifs",
    "_real=$(PATH=$_rest command -v '" +
      escapedProgram +
      "') || { printf 'localterm: " +
      escapedProgram +
      " not found on PATH\\n' >&2; exit 127; }",
    resolveSnippet,
  );
  if (activityFile) {
    lines.push(
      '"$_real" "$@"',
      "_rc=$?",
      'printf \'%s\\n\' "$PWD" > "$_activity_file" 2>/dev/null || :',
      "exit $_rc",
    );
  } else {
    lines.push('exec "$_real" "$@"');
  }
  lines.push("");
  return lines.join("\n");
};

// Regenerate all shims for the current policy: one shim per secret process
// plus one per built-in activity-watched program, and stale shims (names no
// longer in either set) removed. Called on daemon start and after every
// policy change. The shims dir is localterm-owned (~/.localterm/shims) and
// contains only generated shims, so removing any file not in the wanted set
// is safe.
export const regenerateShims = (
  processes: readonly Process[],
  envVarByName: Map<string, string>,
  shimsDir: string,
  backend: SecretBackend,
  activityDir: string,
  activityWatched: readonly string[] = ACTIVITY_WATCHED_PROGRAMS,
): void => {
  if (!backend.supported) {
    // No backend: clear any stale shims and leave nothing behind, so the
    // feature is inert rather than emitting shims that resolve to nothing.
    try {
      for (const name of fs.readdirSync(shimsDir)) {
        fs.rmSync(path.join(shimsDir, name), { force: true });
      }
    } catch {
      // dir absent — nothing to clean
    }
    return;
  }

  const processIndex = buildProcessSecretIndex(processes, envVarByName);
  fs.mkdirSync(shimsDir, { recursive: true, mode: 0o700 });
  fs.mkdirSync(activityDir, { recursive: true, mode: 0o700 });
  // The wanted set is user-configured secret processes plus the built-in
  // activity-watched programs (which get a shim even with no secrets, purely
  // to signal — so detection of short-lived CLIs like `gh` doesn't depend on the
  // user also configuring a secret for them).
  const wantedPrograms = new Set<string>(processIndex.keys());
  for (const program of activityWatched) wantedPrograms.add(program);

  for (const name of fs.readdirSync(shimsDir)) {
    if (!wantedPrograms.has(name)) {
      fs.rmSync(path.join(shimsDir, name), { force: true });
    }
  }

  for (const program of wantedPrograms) {
    const programSecrets = processIndex.get(program) ?? [];
    const resolveSnippet = programSecrets
      .map((secret) => backend.shimResolveSnippet(secret.name, secret.envVar))
      .join("\n");
    const activityFile = activityWatched.includes(program)
      ? path.join(activityDir, program)
      : undefined;
    const content = buildShimContent(program, shimsDir, resolveSnippet, activityFile);
    const shimPath = path.join(shimsDir, program);
    fs.writeFileSync(shimPath, content, { mode: 0o700 });
    // writeFileSync's mode only applies on create; re-chmod so a rewrite of an
    // existing shim with wrong perms is corrected.
    fs.chmodSync(shimPath, 0o700);
  }
};

// The line localterm's shell hook runs AFTER the user's rc files to prepend
// the shims dir to PATH. Running after the rc files is the point: the user's
// rc commonly re-prepends homebrew/mise, which would push a daemon-time PATH
// entry down and let the real binary shadow the shim. The hook runs last, so
// the shims dir wins. The dir's existence gates the prepend (empty feature =
// no PATH pollution).
export const shimPathPrependLine = (shimsDir: string): string => {
  const escaped = shimsDir.replace(/'/g, "'\\''");
  return `[ -d '${escaped}' ] && PATH='${escaped}:'"$PATH" && export PATH`;
};
